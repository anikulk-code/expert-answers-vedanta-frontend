/**
 * Refresh public/data/precanned-answers.json from the answers API.
 *
 * Usage:
 *   node scripts/build-precanned-answers.js
 *
 * Optional env vars:
 *   PRECANNED_API_URL  defaults to production API
 *   PRECANNED_OUTPUT_PATH
 */

const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');

const DEFAULT_API_URL =
  process.env.PRECANNED_API_URL ||
  'https://expertanswersapi-ege8htfcg5a0bgbk.westus2-01.azurewebsites.net';
const DEFAULT_OUTPUT_PATH = path.join(
  __dirname,
  '..',
  'public',
  'data',
  'precanned-answers.json',
);

const PRECANNED_QUESTIONS = [
  'Why should I care about spirituality?',
  'Is consciousness generated by the brain?',
  'How does Vedanta view suffering?',
  'Does Vedanta promote indifference to social problems?',
];

function fetchAnswers(question) {
  const stdout = execFileSync(
    'curl',
    [
      '-sG',
      `${DEFAULT_API_URL}/api/answers/v1`,
      '--data-urlencode',
      `question=${question}`,
      '--data-urlencode',
      'count=5',
    ],
    { encoding: 'utf8' },
  );
  return JSON.parse(stdout);
}

function main() {
  const outputPath = process.env.PRECANNED_OUTPUT_PATH || DEFAULT_OUTPUT_PATH;
  const questions = {};

  for (const question of PRECANNED_QUESTIONS) {
    const response = fetchAnswers(question);
    questions[question] = response;
    console.log(
      `Fetched "${question}" → ${response.answers?.length || 0} answers`,
    );
  }

  const payload = {
    generatedAt: new Date().toISOString().slice(0, 10),
    questions,
  };

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, `${JSON.stringify(payload, null, 2)}\n`);

  const sizeKb = (fs.statSync(outputPath).size / 1024).toFixed(1);
  console.log(`Wrote ${outputPath} (${sizeKb} KB)`);
}

main();
